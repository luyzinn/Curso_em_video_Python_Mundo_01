a=input('Digite alguma coisa: ')

print('1 - Qual o tipo primitivo? ',type(a))
print('2 - Ele é numérico? ', a.isnumeric())
print('3 - Ele é alfabetico?', a.isalpha())
print('4 - Ele é alfanumérico? ',a.isalnum())
print('5 - Ele está em minúsculo? ', a.islower())
print('6 - Ele está em maísculo? ', a.isupper())

